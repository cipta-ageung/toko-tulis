<h4>Sejarah Perusahaan</h4>

Toko SM fotokopi didirikan pada tahun 2015, Yang berlokasi di Jalan Caringin, Kecamatan Pancoran Mas, Kota Depok. Merupakan sebuah toko yang menyediakan Alat tulis kantor ( ATK ), Alat sekolah, Fotokopi, cetak dokumen, cetak foto, penjilidan  dan lain lain. 
Awal mulanya toko ini didirikan hanya fotokopi dan beberapa macam alat tulis sekolah & kantor yang dijual. Pada tahun 2015 hanya memiliki satu mesin fotokopi. Dengan berjalannya waktu toko ini merupakan toko yang berkembang sangat pesat dari tahun ke tahun dimana pada tahun 2016 sudah memiliki 2 mesin foto copy dan kelengkapan alat tulis sekolah dan kantor yang sangat lengkap. 
Toko ini sangat mengedepankan kenyamanan berbelanja bagi para pengunjung dengan fasilitas bangunan yang dilengkapi dengan rak display yang elegan, serta pelayanan yang ramah dan  memuaskan. 


